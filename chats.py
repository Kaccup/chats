    btn_today = types.KeyboardButton('–†–∞—Å—Å–∫–∞–∑')
    btn_tomorrow = types.KeyboardButton('–ê–Ω–µ–∫–¥–æ—Ç')
    btn_joke = types.KeyboardButton('–®—É—Ç–∫–∞')
    btn_badjoke = types.KeyboardButton('–ß–µ—Ä–Ω—ã–π —é–º–æ—Ä')
